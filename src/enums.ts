/**
 * Operators for the search method.
 */
export enum Operators {
  /** Less than operator. */
  LessThan = "<",
  /** Less than or equal to operator. */
  LessThanOrEqual = "<=",
  /** Equal to operator. */
  Equal = "=",
  /** Greater than or equal to operator. */
  GreaterThanOrEqual = ">=",
  /** Greater than operator. */
  GreaterThan = ">",
  /** Not equal to operator. */
  NotEqual = "!=",
}
